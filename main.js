$('.answer').hide(0);

let PlusRotate = false;

$('#plus1').click(function(){
    if(PlusRotate == false){
        $('#plus1').css('transform', 'rotate(45deg)');
        PlusRotate = true;
    }else{
        $('#plus1').css('transform', 'rotate(0deg)');
        PlusRotate = false;
    }
    $('#answer1').slideToggle(300);
});

